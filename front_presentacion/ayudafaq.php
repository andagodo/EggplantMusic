	<h2><u>Ayuda / FAQ</u></h2>
	<p><b>¿Qué es Eggplant Music?</b></br>
	Eggplant Music es una aplicación web multiplataforma que distribuye legalmente música por streming.
	</p>
	<p><b>¿Qué es Streaming?</b></br>
	Streaming, es una tecnología que permite descargar en un pc, teléfono inteligente o tablet contenido de audio o video en un búfer de datos donde se almacena, para inmediatamente mostrar el material descargado aún sin terminar de descargar el archivo completo en el dispositivo del usuario.
	</p>
	<p><b>¿Cómo funciona Eggplant Music?</b></br>
	Para usar Eggplant Music el usuario se debe registrar en nuestro sitio, eligiendo el tipo de cuenta que desea. Una vez enviado el formulario de registro le llegará a su casilla de mail un enlace que una vez confirmado por el usuario habilita el uso del servicio.
	</p>
	<p><b>¿Puedo hacer mejoras en mi cuenta?</b></br>
	Con su cuenta gratuita podrá acceder sin realizar ningún tipo de pago al catálogo completo de artistas y crear hasta 2 listas de reproducción. Se incluye publicidad contratada por nuestros anunciantes.
	</br>Si desea actualizarse a Premium la cuenta requiere un pago mensual y podrá crear hasta 5 listas de reproducción sin publicidad, y si actualiza a una cuenta Vip por un pago anual obtendrá 50% de bonificación con Playlists ilimitadas y sin publicidad.
	</p>
	<p><b>¿Cómo administro mi cuenta?</b></br>
	Una vez registrado en Eggplant Music como usuario la aplicación permite en su página acceder al servicio de streming a través del botón Acceder. Cada usuario dispondrá del catálogo de música para armar sus Playlists.
	</p>
